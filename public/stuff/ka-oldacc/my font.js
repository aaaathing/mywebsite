var charLens = {
  I:4,
  i:2,
  l:2,
  t:4,
  x:5,
  z:5,
  " ":3,
  "-":4,
  "!":2,
  ".":2,
  ",":3,
  "|":3,
  "<":4,
  ">":4,
  ":":2,
  ";":2,
};
function renderChar(what,cx,cy){
    var pixw=textw/6;
    var pixh=texth/6;
    
    var char=chars[what];
    if(chars[what]){
        for(var y=0;y<char.length;y++){
            var row=char[y];
            for(var x=0;x<row.length;x++){
                if(row[x]==="1"){
                    fill(0, 0, 0);
                    rect(x*pixw+cx, y*pixh+cy, pixw,pixh);
                }
            }
        }
    }
    
}
function render(what,x,y){
    var i;
    var cx=x, cy=y;
    for(i=0;i<what.length;i++){
        var c = what[i];
        
        renderChar(c,cx,cy);
        
        if(charLens[c]){
            cx += (charLens[c])*pixW;
        }else{
            cx += textw;
        }
        
        if(cx+textw > width || c==="\n"){
            cx=x;
            cy+=texth;
        }
        
        
    }
}

